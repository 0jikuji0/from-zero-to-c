#include <iostream>

int add(int a, int b) {
    return a + b;
}

int main() {
    int x, y;
    if (!(std::cin >> x >> y)) {
        std::cerr << "Error: expected two integers\n";
        return 1;
    }
    std::cout << add(x, y) << '\n';
    return 0;
}
